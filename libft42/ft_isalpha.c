#include "libft.h"

int	ft_isalpha(int c)
{
	int	resp;

	resp = 1;
	if (c < 65)
		resp = 0;
	else if (c > 90 && c < 97)
		resp = 0;
	else if (c > 122)
		resp = 0;
	return (resp);
}
